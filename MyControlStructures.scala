//simple if statement
object myIf {
   def main(args: Array[String]) {
      var myX = 1040;

      if( myX < 23210 ){
         println("Yay");
      }
   }
}


//if-else statement
object myIfElse {
   def main(args: Array[String]) {
      var myX = 3340;

      if( myX < 2203 ){
         println("Yay");
      } else {
         println("Nay");
      }
   }
}

//if-else-if-else statement
object myIfElseIfElse {
   def main(args: Array[String]) {
      var myX = 345;

      if( myX == 100 ){
         println("Ronaldo");
      } else if( myX == 230 ){
         println("Higuain");
      } else{
         println("Messi");
      }
   }
}
